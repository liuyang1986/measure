package win.lioil.bluetooth.bean;

public class MeasureRecordBean {

    private String measureName;

    private String measureTime;

    private String measureType;

    private String measureStatus;

    private String measureWatcher;

    public String getMeasureName() {
        return measureName;
    }

    public void setMeasureName(String measureName) {
        this.measureName = measureName;
    }

    public String getMeasureTime() {
        return measureTime;
    }

    public void setMeasureTime(String measureTime) {
        this.measureTime = measureTime;
    }

    public String getMeasureType() {
        return measureType;
    }

    public void setMeasureType(String measureType) {
        this.measureType = measureType;
    }

    public String getMeasureStatus() {
        return measureStatus;
    }

    public void setMeasureStatus(String measureStatus) {
        this.measureStatus = measureStatus;
    }

    public String getMeasureWatcher() {
        return measureWatcher;
    }

    public void setMeasureWatcher(String measureWatcher) {
        this.measureWatcher = measureWatcher;
    }
}
