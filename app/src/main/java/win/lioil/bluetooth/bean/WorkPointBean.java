package win.lioil.bluetooth.bean;

public class WorkPointBean {

    private String xmmc = "";

    private String xmid = "";

    private String bdmc = "";

    private String bdid = "";

    private String gdmc = "";

    private String gdid = "";


    public String getXmid() {
        return xmid;
    }

    public void setXmid(String xmid) {
        this.xmid = xmid;
    }

    public String getXmmc() {
        return xmmc;
    }

    public void setXmmc(String xmmc) {
        this.xmmc = xmmc;
    }

    public String getBdid() {
        return bdid;
    }

    public void setBdid(String bdid) {
        this.bdid = bdid;
    }

    public String getBdmc() {
        return bdmc;
    }

    public void setBdmc(String bdmc) {
        this.bdmc = bdmc;
    }

    public String getGdid() {
        return gdid;
    }

    public void setGdid(String gdid) {
        this.gdid = gdid;
    }

    public String getGdmc() {
        return gdmc;
    }

    public void setGdmc(String gdmc) {
        this.gdmc = gdmc;
    }
}
